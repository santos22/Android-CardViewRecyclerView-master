package com.hathy.listsandcards;

class Cleveran {
    String name;
    String position;
    String blurb;
    String image;
    int cleverLogo;

    Cleveran(String name, String position, String blurb, String image, int cleverLogo) {
        this.name = name;
        this.position = position;
        this.blurb = blurb;
        this.image = image;
        this.cleverLogo = cleverLogo;
    }
}