package com.hathy.listsandcards;

class Person {
    String name;
    String age;
    String blurb;
    String image;
    int photoId;

    Person(String name, String age, String blurb, String image, int photoId) {
        this.name = name;
        this.age = age;
        this.blurb = blurb;
        this.image = image;
        this.photoId = photoId;
    }
}